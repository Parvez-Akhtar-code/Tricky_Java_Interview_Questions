package QuesAndAns;

public class Q1_PrintOneToHundred {
    /* print 1 to 100 without using any number */

    public static void main(String[] args) {
        int a='A'/'A';

        int b = 'd';

        for(int i=a;i<=b;i++){
            System.out.println(i);
        }
        System.out.println();
        String s = "helloworld";
        for(int i=a;i<=(s.length()*s.length());i++){
            System.out.println(i);
        }

    }
}
